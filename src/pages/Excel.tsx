import React, { useEffect, useState } from "react";
import { read, utils, writeFile } from "xlsx";

export function Excel() {
  /* the component state is an array of objects */
  const [pres, setPres] = useState<any[]>([]);
  const [file, setFile] = useState<File | null>(null);

  /* Fetch and update the state once */
  useEffect(() => {
    (async () => {
      /* Download from https://docs.sheetjs.com/pres.numbers */
      const f = await fetch("https://docs.sheetjs.com/pres.numbers");
      const ab = await f.arrayBuffer();

      /* parse */
      const wb = read(ab);

      /* generate array of presidents from the first worksheet */
      const ws = wb.Sheets[wb.SheetNames[0]]; // get the first worksheet
      const data = utils.sheet_to_json(ws); // generate objects

      /* update state */
      setPres(data); // update state
    })();
  }, []);

  // console.log(pres);

  return (
    <div style={{ display: "grid", gap: "12px", padding: "50px" }}>
      <input
        type="file"
        onChange={(ev) => {
          let f = ev.target.files?.[0] || null;

          if (f) {
            setFile(ev.target.files?.[0] || null);

            f.arrayBuffer().then((buff) => {
              console.log(buff);
              let start = Date.now();
              let res = read(buff, {
                sheetRows: 400,
                cellHTML: false,
                cellFormula: false,
                // UTC: false,
              });

              console.log("read, ms", Date.now() - start);
              console.log(res.Sheets.Sheet1);
              let rows = Object.keys(res.Sheets.Sheet1)
                .map((it) => it[0])
                .toSorted()
                .filter((cur, i, all) => {
                  return i > 0 && cur !== all[i - 1];
                });
              let ARR = Array(400)
                .fill(0)
                .map((r, rowI) =>
                  rows
                    .map((row) => {
                      return res.Sheets.Sheet1[row + (rowI + 1)]?.v || null;
                    }),
                );
              console.log(JSON.stringify(ARR));

              // console.log(res.Sheets.Sheet1["!cols"]);
              // console.log(res.Sheets.Sheet1.C135);
              // console.log(utils.sheet_to_json(res, { header: "A" }));

              // console.log(res.Sheets.Sheet1.B6);
            });
          }
        }}
      />
      <p>13123</p>
    </div>
  );
}

